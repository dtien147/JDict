    private final ActionListener searchWord = new ActionListener() {
      @Override
      public void actionPerformed(ActionEvent e) {
        String word = ((JMenuItem) e.getSource()).getText();
        txtWord.setText(word);
        search();
      }
    };        
    
    private void txtWordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtWordKeyReleased
        String word = txtWord.getText();
        JPopupMenu popup = new JPopupMenu();    
        ArrayList<String> similarWords = dict.findSimilars(word);
        for(int i = 0; i < similarWords.size(); i++) {
            JMenuItem item = new JMenuItem(similarWords.get(i));
            item.addActionListener(searchWord);
            popup.add(item);            
        }                    
        
        popup.show(txtWord, txtWord.getX(), txtWord.getY());
        
        txtWord.requestFocus();
    }//GEN-LAST:event_txtWordKeyReleased

