    private void btnAddToFavoriteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToFavoriteActionPerformed
        String word = txtWord.getText();
        if(dict.findWord(word) >= 0 && favDict.findWord(word) < 0) {
            int wordIndex = dict.findWord(word);
            Record record = dict.getItem(wordIndex);
            favDict.add(record);
            favDict.save();
        }
    }//GEN-LAST:event_btnAddToFavoriteActionPerformed

