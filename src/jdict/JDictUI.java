    private void statistics(String word) {            
        wordStatistics.add(new Statistics(word));
        saveStatistics();
    }
    
    private void saveStatistics() {
        try (FileOutputStream fileOut = new FileOutputStream(Global.STATISTICS_FILE_PATH)) {
            ObjectOutputStream output = new ObjectOutputStream(fileOut);
            output.writeObject(wordStatistics);
            output.close();
        } catch (Exception ex) {
            System.out.println(ex);
        }        
    }
    
    private void search() {
        String word = txtWord.getText();
        
        int indexOfWord;
        
        if((indexOfWord = dict.findWord(word)) >= 0) {
            Record record = dict.getItem(indexOfWord);
            txtMeaning.setText(record.getMeaning());
            
            statistics(word);
        }
    }
    
